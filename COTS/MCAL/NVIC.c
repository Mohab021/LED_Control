/******************************************************************************
* \Syntax          : void Ext_Interrupt_GPIOF_Init(void)
* \Description     : Configure PIN0 and PIN4 of PORTB for falling edge trigger interrupt
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : None
* \Parameters (out): None
* \Return value:   : None
*******************************************************************************/
void Ext_Interrupt_GPIOF_Init(void)
{
	//Configure PIN0 and PIN4 in PORTF for falling edge trigger interrupt
	
	//Make PIN0 and PIN4 edge sensitive
	GPIO_PORTF_IS_R &= ~(1<<0) | ~(1<<4);
		
	//Falling edge trigger
	GPIO_PORTF_IBE_R &= ~(1<<0) | ~(1<<4);
	GPIO_PORTF_IEV_R &= ~(1<<0) | ~(1<<4);
	
	//Clear any prior interrupt
	GPIO_PORTF_ICR_R |= (1<<0) | (1<<4);
	
	//Unmask interrupt
	GPIO_PORTF_IM_R |= (1<<0) | (1<<4);
	
	//Enable IRQ30 in NVIC
	NVIC_EN0_R |= 1<<30;
}

