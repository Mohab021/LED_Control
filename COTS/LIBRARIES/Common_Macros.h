#define Clear_Bit(Reg_Name, Bit_Pos) (Reg_Name &= ( ~(1 << Bit_Pos) ))
#define Read_Bit(Reg_Name, Bit_Pos) ((Reg_Name & (1 << Bit_Pos)) >> Bit_Pos)
#define Toggle_Bit(Reg_Name, Bit_Pos) (Reg_Name ^= (1 << Bit_Pos))

